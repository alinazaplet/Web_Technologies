// Функція для пошуку слова в словнику
function searchWord() {
  const input = document.getElementById("wordInput").value.trim().toLowerCase(); // Отримуємо і форматуюємо значення поля введення
  const resultDiv = document.getElementById("result");

  // Перевіряємо, чи введене значення не порожнє та не є числом
  if (input === "" || !isNaN(input)) {
      resultDiv.textContent = "Будь ласка, введіть дійсне слово.";
      return;
  }

  // Шукаємо слово в словнику
  const definition = dictionary[input];
  if (definition) {
      resultDiv.innerHTML = `<strong>Пояснення:</strong> ${definition}`;
  } else {
      resultDiv.textContent = "Слово не знайдено.";
  }

  // Очищуємо поле введення
  document.getElementById("wordInput").value = "";
}
